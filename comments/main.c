#include <stdio.h>
#include <stdlib.h>

int main()
{
    /*
        Block comment
        spanning multiple
        lines.
    */
    printf("I have a block comment above me!\n");

    // this is a single line comment. Contents ignored until new line
    printf("I have a single line comment above me and to the right of me!\n"); // this is also ignored
    return 0;
}